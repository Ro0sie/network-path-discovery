from lab04 import network_path_exists

def test_path_exists():
    net = [
        ['+', '+', '+', '+', 'G', '+'],
        ['+', ' ', '+', ' ', ' ', '+'],
        ['+', ' ', ' ', ' ', '+', '+'],
        ['+', ' ', '+', '+', ' ', '+'],
        ['+', ' ', ' ', ' ', ' ', '+'],
        ['+', '+', '+', '+', '+', '+'],
    ]
    assert network_path_exists(net, 4, 4) == True

def test_no_path():
    net = [
        ['+', '+', '+', '+', 'G', '+'],
        ['+', '+', '+', '+', '+', '+'],
        ['+', '+', '+', '+', '+', '+'],
        ['+', '+', '+', '+', '+', '+'],
        ['+', ' ', ' ', ' ', ' ', '+'],
        ['+', '+', '+', '+', '+', '+'],
    ]
    assert network_path_exists(net, 4, 1) == False
